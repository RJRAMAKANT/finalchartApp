package com.beanclass;

public class GraphMonthWise {
	private String Cenercode;
	private String centeryear;
	private String Month;
	private String TotalCases;
	public String getCenercode() {
		return Cenercode;
	}
	public void setCenercode(String cenercode) {
		Cenercode = cenercode;
	}
	public String getCenteryear() {
		return centeryear;
	}
	public void setCenteryear(String centeryear) {
		this.centeryear = centeryear;
	}
	public String getMonth() {
		return Month;
	}
	public void setMonth(String month) {
		Month = month;
	}
	public String getTotalCases() {
		return TotalCases;
	}
	public void setTotalCases(String totalCases) {
		TotalCases = totalCases;
	}
	

}
